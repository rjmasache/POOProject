    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String nameSearch = txtSearch.getText();
        for (Product product : Register.products) {
            // Verificación del nombre a buscar que esté contenido
            if (product.getName().contains(nameSearch)){
                lblName.setText(product.getName());
                lblMeasure.setText(product.getMeasure());
                lblStock.setText(String.valueOf(product.getQuantity()));
                lblValue.setText(String.valueOf(product.getValue()));
            }
        }
    }//GEN-LAST:event_searchActionPerformed

