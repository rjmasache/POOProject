    private void billActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_billActionPerformed
        // TODO add your handling code here:
        // Transformación de variables
        quantity = Integer.parseInt(txtQuantity.getText());
        deposit = Integer.parseInt(lblStock.getText());
        value = Double.parseDouble(lblValue.getText());
        lblSubTotal.setText("0");
        // Estructura condicional para evaluar el valor dle producto si se encuentra en Stock
        if (quantity <= deposit){
            sTotal = quantity * value;
            deposit = deposit - quantity;
        } else {
            // Mensaje presentado cuando el Stock es insuficiente
            result = "The quantity of products in Stock is insufficient";
            lblMessage.setText(String.valueOf(result));
        }
        // Cálculo de precios a pagar
        // Cálculo del IVA
        vat = sTotal * 0.12;
        // Cálculo del precio total del producto
        total = sTotal + vat;
        // Cálculo del valor total a pagar
        totalValue = totalValue + total;
        // Uso de métodos para mostrar los resultados
        lblSubTotal.setText(String.valueOf(sTotal));
        lblVAT.setText(String.valueOf(vat));
        lblTotal.setText(String.valueOf(total));
        lblTotalValue.setText(String.valueOf(totalValue));
    }//GEN-LAST:event_billActionPerformed

